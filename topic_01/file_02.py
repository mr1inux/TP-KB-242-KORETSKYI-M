w0 = "  Hello Max   "
w1 = "you are welcome"
w2 = "LOWER WORD"
print(w0.strip())  # забирає пробіли
print(w2.capitalize())  # перша літера велика, решта маленькі
print(w1.title())  # велика тільки перша буква слова
print(w0.upper())  # всі великі букви
print(w2.lower())  # всі малі букви
print(w0.swapcase())  # міняє великі літері на малі і навпаки
